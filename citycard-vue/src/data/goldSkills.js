/**
 * 获取技能的完整信息
 * @param {string} skillName - 技能名称
 * @returns {Object|null} 技能信息对象 {name, cost, description}
 */
export function getSkillInfo(skillName) {
  if (!SKILL_COSTS.hasOwnProperty(skillName)) {
    return null
  }

  return {
    name: skillName,
    cost: SKILL_COSTS[skillName] || 0,
    description: SKILL_DESCRIPTIONS[skillName] || '暂无描述'
  }
}

/**
 * 判断技能是否为战斗技能
 * @param {string} skillName - 技能名称
 * @returns {boolean}
 */
export function isBattleSkill(skillName) {
  const battleSkillNames = BATTLE_SKILLS.map(s => s.name)
  return battleSkillNames.includes(skillName)
}
